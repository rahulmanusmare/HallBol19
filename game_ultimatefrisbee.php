		<h3>Game Rules</h3>
		<ul>
		<li><b>Starting the Game: </b></li>
		       <ul>
			   <li>A coin toss allots the two teams as offense (in possession of the disc) and defense (not in possession of the disc).</li>
			   <li>Players from both teams take positions on the D-lines of their respective goals.</li>
			   <li>Initially the defense has the possession of the frisbee.</li>
			   <li>The defense throws the frisbee to the offense and the game starts.</li>
			   <li>When the defense score, the defense becomes the offense team and sides switch.</li>
			    </ul>
		 <li><b>Passing:</b></li>
		        <ul>
				<li>On the first throw of the game or on a throw after any goal scored, a defense player cannot possess the frisbee on first flight. Even if the frisbee touches the ground, offense gets the first possession.</li>
				<li>Disc can be passed in any direction.</li>
				<li>If the disc goes beyond the borders on the field, it is considered an outside and the possession switches.</li>
				<li>If a player drops the disc, the possession switches</li>
				<li>A defense player has to maintain a minimum distance of 1 arms length from the offense player in possession of the disc. If not so, will be considered as a foul.</li>
				<li>Offense player is not allowed to run from the position where he/she caught it with the disc still in hand. If so, will be counted as a foul.</li>
				<li>Offense player has only 10 seconds to throw the disc. If not so, will be counted as a foul.</li>
				<li>If a defense player taps the disc while it is in flight, the possession switches, defense becomes offense and vice-versa.</li>
				<li>If a defense player taps the disc while it is in the hands of an offense player, it is counted as a foul.</li>
				<li>In case of any of the fouls mentioned above, the possession switches immediately to the opposite team.<li>
				<li>If a player does 3 fouls, he is out of the game for the whole 30 minutes</li>
				
				</ul></li></li></ul>
<h3>Scoring:</h3>
There are three lines: Goal-line and Free-throw-line.
Once inside the D-line, the frisbee has to be caught (not tapped) outside the Goal-line (for boys) and it has to be at least tapped (can be caught too) outside the Goal-line (for girls). This is considered a goal.
For every goal scored, the team gets 5 points.
On scoring a goal, whoever Rxcaught the frisbee last (for boys) and whoever tapped the frisbee last (for girls) gets a free throw (no goalkeeper) at the goal post (on the same side as the goal scored) from the Free-throw-line. If the frisbee lands inside the goal post, the team gets an additional 2 points.
After every goal scored, teams take respective positions on their D-lines.
<br>
<h3>Physical Contact:</h3>
No intentional physical contact is allowed between the players (referee’s decision will be final in this regard, no arguments allowed). If so, it will be counted as a foul and a penalty will be awarded to the opposition wherein they can take a shot from the Free-throw-line at the nearest goal post. The game resumes irrespective of a goal scores or not and the possession remains with the opposition.
<br>
<h3>Substitutions:</h3>
The total game time is 30 minutes with 2 halves of 12 minutes each and a gap of 6 minutes between the two halves.
Substitutes may replace players on field either after a goal is scored or during an injury timeout or in the second half.

