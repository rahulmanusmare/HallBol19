		   <h3>Game Rules</h3>
               <p>Two teams of seven players (six field players plus one goalkeeper) take the field and attempt to score points by putting the game ball into the opposing team's goal. In handling the ball, players are subject to the following restrictions:
After receiving the ball, players can pass, keep possession, or shoot the ball.
If possessing the ball, players must dribble (similar to a basketball dribble), or can take up to three steps for up to three seconds at a time without dribbling.
No attacking or defending players other than the defending goalkeeper are allowed to touch the floor of the goal area (within six metres of the goal). A shot or pass in the goal area is valid if completed before touching the floor. Goalkeepers are allowed outside the goal area, but are not allowed to cross the goal area boundary with the ball in their hands.
The ball may not be passed back to the goalkeeper when they are positioned in the goal area.</p>
		<ul>
				    <li><b>Playing field</b></b>
					<p>Handball is played on a court 40 by 20 metres (131 ft × 66 ft), with a goal in the center of each end. The goals are surrounded by a near-semicircular area, called the zone or the crease, defined by a line six meters from the goal. A dashed near-semicircular line nine metres from the goal marks the free-throw line.</p>
					<li><b>Duration</b></li>
					<p>A standard match for all teams of 16 and older has two periods of 30 minutes with an interval of 10–15 minutes.</p>
					<li><b>Field player</b></li>
					<p>Field players are allowed to touch the ball with any part of their bodies above and including the knee. As in several other team sports, a distinction is made between catching and dribbling. A player who is in possession of the ball may stand stationary for only three seconds, and may take only three steps. They must then either shoot, pass, or dribble the ball. Taking more than three steps at any time is considered travelling, and results in a turnover. A player may dribble as many times as they want (though, since passing is faster, it is the preferred method of attack), as long as during each dribble the hand contacts only the top of the ball. Therefore, carrying is completely prohibited, and results in a turnover. After the dribble is picked up, the player has the right to another three seconds or three steps. The ball must then be passed or shot, as further holding or dribbling will result in a double dribble turnover and a free throw for the other team. Other offensive infractions that result in a turnover include charging, setting an illegal screen, or carrying the ball into the six-meter zone.</p>
					<li><b>Awarded throws</b></li>
					<p>The referees may award a special throw to a team. This usually happens after certain events such as scored goals, off-court balls, turnovers and timeouts. All of these special throws require the thrower to obtain a certain position, and pose restrictions on the positions of all other players. Sometimes the execution must wait for a whistle blow by the referee.</p>
					<li><b>Throw-off</b></li>
					<p>A throw-off takes place from the center of the court. The thrower must touch the middle line with one foot, and all the other offensive players must stay in their half until the referee restarts the game. The defending players must keep a distance of at least three meters from the thrower. A throw-off occurs at the beginning of each period and after the opposing team scores a goal. It must be cleared by the referees.</p>
					<li><b>Throw-in</b></li>
					<p>The team which did not touch the ball last is awarded a throw-in when the ball fully crosses the side line or touches the ceiling. If the ball crosses the outer goal line, a throw-in is awarded only if the defending field players touched the ball last. Execution requires the thrower to place one foot on the nearest outer line to the cause. All defending players must keep a distance of three meters. However, they are allowed to stand immediately outside their own goal area even when the distance is less than three meters.</p>
					<li><b>Goalkeeper-throw</b></li>
					<p>If the ball crosses the outer goal line without interference from the defending team or when deflected by the defending team's goalkeeper, or when the attacking team violates the D-zone as described above, a goalkeeper-throw is awarded to the defending team. This is the most common turnover. The goalkeeper resumes the play with a throw from anywhere within the goal area.</p>
					<li><b>Free-throw</b></li>
					<p>A free-throw restarts the play after an interruption by the referees. It takes places from the spot where the interruption was caused, as long as this spot is outside of the free-throw line of the opposing team. In the latter case, the throw is deferred to the nearest spot on the free-throw line. Free-throws are the equivalent to free-kicks in association football</p>
					<li><b>Seven-meter throw</b></li>
					<P>A seven-meter throw is awarded when a clear chance of scoring is illegally prevented anywhere on the court by an opposing team player, official, or spectator. It is awarded also when the referees have interrupted a legitimate scoring chance for any reason. The thrower steps with one foot behind the seven-meter line with only the defending goalkeeper between him and the goal. The goalkeeper must keep a distance of three meters, which is marked by a short tick on the floor. All other players must remain behind the free-throw line until execution. The thrower must await the whistle blow of the referee. A seven-meter throw is the equivalent to a penalty kick in association football; however, it is far more common and typically occurs several times in a single game.</p>
					<li><b>Penalties</b></li>
					<p>Penalties are given to players, in progressive format, for fouls that require more punishment than just a free-throw. Actions directed mainly at the opponent and not the ball (such as reaching around, holding, pushing, hitting, tripping, and jumping into opponent) as well as contact from the side, from behind a player or impeding the opponent's counterattack are all considered illegal and are subject to penalty. Any infraction that prevents a clear scoring opportunity will result in a seven-meter penalty shot.
Typically the referee will give a warning yellow card for an illegal action; but, if the contact was particularly dangerous, like striking the opponent in the head, neck or throat, the referee can forego the warning for an immediate two-minute suspension. A player can get only one warning before receiving a two-minute suspension. One player is only permitted two two-minute suspensions; after the third time, they will be shown the red card.
A red card results in an ejection from the game and a two-minute penalty for the team. A player may receive a red card directly for particularly rough penalties. For instance, any contact from behind during a fast break is now being treated with a red card. A red-carded player has to leave the playing area completely. A player who is disqualified may be substituted with another player after the two-minute penalty is served.
After having lost the ball during an attack, the ball has to be laid down quickly or else the player not following this rule will face a two-minute suspension. Also, gesticulating or verbally questioning the referee's order, as well as arguing with the officials' decisions, will normally result in a two-minute suspension. If this is done in a very provocative way, the player can be given a double two-minute suspension if they do not walk straight off the field to the bench after being given a suspension, or if the referee deems the tempo deliberately slow. Illegal substitution (that is, any substitution that does not take place in the specified substitution area, or where the entering player enters before the exiting player exits) is also punishable by a two-minute suspension.
					
Modern handball introduced the "fast throw-off" concept; i.e., the play will be immediately restarted by the referees as soon as the executing team fulfills its requirements. Many teams leverage this rule to score easy goals before the opposition has time to form a stable defense line.</P>
